(function initPortfolio() {
  const storageKey = 'lang';
  const siteHeader = document.querySelector('.site-header');
  const menuToggle = document.querySelector('.menu-toggle');
  const navLinks = document.querySelectorAll('.site-nav a');
  const year = document.getElementById('year');
  let currentLanguage = 'en';

  if (year) {
    year.textContent = new Date().getFullYear();
  }

  const savedLanguage = getSavedLanguage();
  applyLanguage(savedLanguage);

  document.querySelectorAll('[data-lang-btn]').forEach((button) => {
    button.addEventListener('click', () => {
      const language = button.dataset.langBtn;
      applyLanguage(language);
    });
  });

  if (menuToggle && siteHeader) {
    menuToggle.addEventListener('click', () => {
      setMenuOpen(!siteHeader.classList.contains('is-open'));
    });

    navLinks.forEach((link) => {
      link.addEventListener('click', () => closeMenu());
    });

    document.addEventListener('keydown', (event) => {
      if (event.key === 'Escape') {
        closeMenu();
      }
    });
  }

  function applyLanguage(language) {
    const lang = translations[language] ? language : 'en';
    const dictionary = translations[lang];
    currentLanguage = lang;

    document.documentElement.setAttribute('lang', lang === 'pt' ? 'pt-BR' : 'en');
    document.documentElement.setAttribute('data-lang', lang);
    document.title = dictionary['meta.title'];

    document.querySelectorAll('[data-i18n]').forEach((node) => {
      const key = node.dataset.i18n;
      if (dictionary[key]) {
        node.textContent = dictionary[key];
      }
    });

    document.querySelectorAll('[data-i18n-content]').forEach((node) => {
      const key = node.dataset.i18nContent;
      if (dictionary[key]) {
        node.setAttribute('content', dictionary[key]);
      }
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach((node) => {
      const key = node.dataset.i18nAriaLabel;
      if (dictionary[key]) {
        node.setAttribute('aria-label', dictionary[key]);
      }
    });

    document.querySelectorAll('[data-lang-btn]').forEach((button) => {
      button.setAttribute('aria-pressed', String(button.dataset.langBtn === lang));
    });

    saveLanguage(lang);
    updateMenuLabel();
  }

  function closeMenu() {
    setMenuOpen(false);
  }

  function setMenuOpen(isOpen) {
    if (!siteHeader || !menuToggle) return;
    siteHeader.classList.toggle('is-open', isOpen);
    menuToggle.setAttribute('aria-expanded', String(isOpen));
    updateMenuLabel();
  }

  function updateMenuLabel() {
    if (!siteHeader || !menuToggle) return;
    const isOpen = siteHeader.classList.contains('is-open');
    const dictionary = translations[currentLanguage] || translations.en;
    const key = isOpen ? 'nav.toggleClose' : 'nav.toggleOpen';
    menuToggle.setAttribute('aria-label', dictionary[key]);
  }

  function getSavedLanguage() {
    try {
      const saved = localStorage.getItem(storageKey);
      if (translations[saved]) return saved;
    } catch (error) {
      return getBrowserLanguage();
    }

    return getBrowserLanguage();
  }

  function saveLanguage(language) {
    try {
      localStorage.setItem(storageKey, language);
    } catch (error) {
      return undefined;
    }

    return undefined;
  }

  function getBrowserLanguage() {
    return (navigator.language || 'en').toLowerCase().startsWith('pt') ? 'pt' : 'en';
  }
})();
